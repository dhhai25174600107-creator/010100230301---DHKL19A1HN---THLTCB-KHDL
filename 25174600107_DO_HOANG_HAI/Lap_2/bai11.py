n = int(input("Nhap n (n > 10): "))

a = 1
S1 = 0
S3 = 0
S4 = 0

while a <= n:
    S1 = S1 + 6 ** a
    S3 = S3 + ((-1) ** a) * (a ** 2)
    S4 = S4 + a * (a + 1) * (a + 2)
    a = a + 1

print("S1 = 6 + 6^2 + 6^3 + ... + 6^n =", S1)
print("S3 = -1^2 + 2^2 - 3^2 + ... + (-1)^a . a^2 =", S3)
print("S4 = 1.2.3 + 2.3.4 + ... + a.(a+1)(a+2) =", S4)
